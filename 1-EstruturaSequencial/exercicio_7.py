#Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
lado = float(input("Digite um dos lados do quadrado: "))
area_da_quadrado = lado ** 2
dobro_da_area  = area_da_quadrado * 2

print(f"A area do quadro tem  o tamanho de {area_da_quadrado}")
print(f"O drobro dessa area e {dobro_da_area}")